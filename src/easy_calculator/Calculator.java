package easy_calculator;

public class Calculator {

	static double plus (double x, double y) {
		double result = x + y;
		return result;
	}
	
	static double minus (double x, double y) {
		double result = x - y;
		return result;
	}
	
	static double multiple (double x, double y) {
		double result = x * y;
		return result;
	}
	
	static double divide (double x, double y) {
		double result = x / y;
		return result;
	}
}
