package ch99_exercise;

public class Pointer2D {

	private int x;
	private int y;
	
	public Pointer2D(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public void setX (int x) {
		this.x = x;
	}
	public int getX () {
		return this.x;
	}
	
	public void setY (int y) {
		this.y = y;
	}
	public int getY () {
		return this.y;
	}
}
